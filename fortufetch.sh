get_gpu_info() {
    local gpus=()
    while IFS= read -r line; do
        local gpu_name=$(echo "$line" | cut -d ':' -f3 | sed 's/^[ \t]*//;s/\s*$//')
        if [[ -n "$gpu_name" ]]; then
            if [[ "$gpu_name" == *"Intel"* ]] || [[ "$gpu_name" == *"AMD"* && "$gpu_name" != *"Radeon"* ]]; then
                gpus+=("${gpu_name} [Integrated]")
            else
                gpus+=("${gpu_name} [Discrete]")
            fi
        fi
    done < <(lspci | grep -i 'vga\|3d\|2d')
    
    # –û–±—ä–µ–¥–∏–Ω—è–µ–º –≤—Å–µ –≤–∏–¥–µ–æ–∫–∞—Ä—Ç—ã –≤ –æ–¥–Ω—É —Å—Ç—Ä–æ–∫—É —Å –ø–µ—Ä–µ–Ω–æ—Å–∞–º–∏
    local result=""
    for ((i=0; i<${#gpus[@]}; i++)); do
        if [ $i -eq 0 ]; then
            result+="${gpus[$i]}"
        else
            result+="\n              ${SECONDARY}${gpus[$i]}"
        fi
    done
    
    # –ï—Å–ª–∏ –≤–∏–¥–µ–æ–∫–∞—Ä—Ç—ã –Ω–µ –Ω–∞–π–¥–µ–Ω—ã
    if [ -z "$result" ]; then
        result="Unknown"
    fi
    
    echo -e "$result"
}

